# integers

print(2 + 3)  # 5
print(3 - 2)  # 1
print(2 * 3)  # 3
print(3 / 2)  # 1.5

print(3 ** 2)  # 9 (exponent)
print(2 + 3 * 4)  # 14 (order of operations
print((2 + 3) * 4)  # 20

# floats

print(.1 + .1)  # .2
print(.2 + .1)  # 0.30000000000000004
print(3 * .01)  # 0.30000000000000004

# underscores in numbers

universe_age = 14_000_000_000
print(universe_age)

# multiple assignments

x, y, z = 1, 2, 3
print(x, y, z)

# capital letters treated as constants
STAY_FOREVER = 100
print(STAY_FOREVER)
